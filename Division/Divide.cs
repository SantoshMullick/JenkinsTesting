using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Division
{
    public class Divide
    {
        public int iDivide(int num1, int num2)
        {
            return num1 / num2;
        }

        public float fDivide(float num1, float num2)
        {
            return num1 / num2;
        }
    }
}
