using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Multiplication
{
    public class Multiply
    {
        public int iMultiply(int num1, int num2)
        {
            return num1 * num2;
        }

        public float fMultiply(float num1, float num2)
        {
            return num1 * num2;
        }
    }
}

