using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Addition
{
    public class Add
    {
        public int iAdd(int num1, int num2)
        {
            return num1 + num2;
        }

        public float fAdd(float num1, float num2)
        {
            return num1 + num2;
        }
    }
}
