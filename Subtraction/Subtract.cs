using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Subtraction
{
    public class Subtract
    {
        public int iSubtract(int num1, int num2)
        {
            return num1 - num2;
        }

        public float fSubtract(float num1, float num2)
        {
            return num1 - num2;
        }
    }
}
